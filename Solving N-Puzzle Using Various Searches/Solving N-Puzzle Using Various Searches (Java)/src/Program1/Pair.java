package Program1;

public class Pair{ // class to keep cordinates for manhattan distance
    int rowCord;
    int colCord;

    Pair(){
        int rowCord = 0;
        int colCord = 0;
    }

    Pair(int tRowCord, int tColCord)
    {
        rowCord = tRowCord;
        colCord = tColCord;
    }

}
